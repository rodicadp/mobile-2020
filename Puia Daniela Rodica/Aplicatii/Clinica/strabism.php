        <p><strong><span>CELE MAI FRECVENTE TIPURI DE STRABISM:</span></strong></p>
        <ul>
            <li>Esotropia congenitala este intalnita frecvent. Este un strabism convergent ce apare in primele luni de viata. Deviatia orizontala este frecvent asociata si cu o deviatie verticala sau cu nistagmus.Tratamentul este aproape exclusiv chirurgical, de cele mai multe ori fiind necesara mai mult de o operatie datorita complexitatii acestui strabism.</li>
            <li>Esotropia acomodativa este un strabism convergent ce apare de cele mai multe ori, dupa varsta de 2 ani. Este insotita de defecte de refractie, cel mai frecvent de hipermetropie. Efortul de acomodatie pe care copilul il face pentru a vedea clar determina deviatia catre nas a ochilor. Aceasta deviatie poate apare atunci cand copilul priveste la distanta sau la aproape sau in ambele situatii. Ochelarii scad efortul de acomodatie si corecteaza pozitia ochilor. Cateodata sunt necesare lentile bifocale sau progresive pentru efortul la aproape. Daca deviatia nu se reduce semnificativ in timpul purtarii corectiei, este indicata chirurgia.</li>
            <li>Exotropia deviatia catre inafara a ochilor, este un alt tip de strabism intalnit frecvent. Aceasta deviatie apare cel mai des atunci cand copilul priveste la distanta. Exotropia poate fi prezenta doar in anumite momente ale zilei, atunci cand copilul este obosit sau neatent. Deseori, parintii remarca aparitia deviatiei doar in lumina puternica. Desi ochelarii, exercitiile, ocluzia unui ochi sau prismele imbunatatesc controlul reducand deviatia, interventia chirurgicala este de multe ori necesara.</li>
        </ul>
