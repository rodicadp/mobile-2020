        <h1 class="s-titluri">Strabism</h1>
        <p><strong><span>CE ESTE STRABISMUL?</span></strong></p>
        <p>Strabismul este o afectiune oculara in care axele celor doi ochi nu sunt paralele si unul sau ambii ochi deviaza. Se estimeaza ca 5-20% din copii au probleme de vedere, iar 5 % din copii au strabism. Principalul semn este lipsa alinierii axelor vizuale ale ochilor, adica un ochi fuge din pozitia normala de drept inainte. Alteori, sunt semne indirecte cum ar fi inchiderea unuia dintre ochi in lumina puternica, intoarcerea sau inclinarea capului intr-o parte.</p>
        <p><strong><span>CARE SUNT CAUZELE STRABISMULUI?</span></strong></p>
        <p>Cauzele strabismului sunt diverse:
            <ul>
                <li>Ereditatea: parintii, fratii sau rudele apropiate au strabism, dar poate sa apara si la copii fara rude cu strabism.</li>
                <li>Viciile de refractie (prezenta dioptriilor), mai ales cand sunt diferente de dioptrii intre ochi. De multe ori, viciile refractie se transmit ereditar.</li>
                <li>Vederea scazuta a unui ochi din diverse motive (leziuni ale fundului de ochi, cataracta, tumori intraoculare)</li>
                <li>Prematurite, dismaturitate</li>
                <li>Afectiuni neurologice: hidrocefalie, sindrom Down, paralizie cerebrala</li>
                <li>Afectiuni congenitale ale orbitei sau ochiului</li>
                <li>Traumatisme cerebrale, ale orbitei sau ale ochiului</li>
                <li>Cauze necunoscute: de cele mai multe ori nu se poate stabili cauza. Strabismul poate sa apara la copii fara alte afectiuni asociate.</li>
            </ul>
        </p>
