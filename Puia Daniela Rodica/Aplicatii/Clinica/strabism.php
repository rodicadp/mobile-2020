        <p><strong><span>OPERATIA DE STRABISM:</span></strong></p>
        <p>Operatia pentru strabism are nu numai un scop estetic, de realiniere a ochilor, ci si un scop functional, mai important, de inlaturare a vederii duble, cand acesta exista, sau de recuperare a vederii binoculare. Cand strabismul apare sub varsta de 6 luni, operatia este indicata sub varsta 1 an pentru sanse optime de recuperare a vederii binoculare. Daca debutul este peste aceasta varsta, medicul oftalmolog pediatru stabileste daca este necesara operatia sau sunt suficiente metodele de tratament ne-chirurgical. Oricum, daca strabimul nu se rezolva in cateva luni prin purtarea ochelarilor, se recomanda operatia, cat mai devreme. Cu cat amanam momentul operatiei cu atat scad sansele de recuperare a vederii binoculare. Operatia poate fi facuta atat la un ochi cat si la ambii ochi, decizia asupra felului interventiei se ia in functie de tipul de strabism. Interventiile de strabism se fac in anestezie generala (foarte rar, daca exista anumite contraindicatii sau daca pacientul nu doreste acest lucru operatia se va face in anestezie loco-regionala, la varsta adulta). Interventia nu necesita spitalizare peste noapte, practic pacientul se opereaza si pleaca acasa in aceeasi zi. Tratamentul nu incheie insa cu interventia chirurgicala. Evolutia trebuie urmarita pana la adolescenta. Obtinerea vederii binoculare este cel mai dificil deziderat. Daca nu tratam ambliopia in primele luni de viata sau amanam operatia sansele de binocularitate scad. Daca toate etapele de terapie sunt instituite la timp avem sanse de pozitie corecta o ochilor si binocularitate cit mai aproape de normal. Operatia de strabism la adulti: Tehnica operatiei este aceeasi ca pentru copii si se desfasoara in anestezie generala. Operatia se face la ochiul strabic, la ochiul fixator sau la ambii, in functie de caz. Daca unghiul de deviatie este mare, poate sa apara necesitatea unei a doua operatii. Operatia poate fi facuta la orice varsta.
        </p>
